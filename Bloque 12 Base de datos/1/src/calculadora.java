public class calculadora {
    private double número1;
    private double número2;

    public calculadora(double número1, double número2) {
        this.número1 = número1;
        this.número2 = número2;
    }
    
    
    
    public double sumar(double número1, double número2){
        return número1 + número2;
    }
    
}
